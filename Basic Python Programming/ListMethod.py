nums=[30,15,50]

nums.append(40) #Add one element at the end.
print(nums)


nums.insert(2,45) #Insert element at the index(middle,first)
print(nums)


nums.sort()
print(nums) #Sort element in increasing order


nums.sort(reverse=True)
print(nums) #Sort element in decreasing order 


nums.reverse()
print(nums) # To reverse list